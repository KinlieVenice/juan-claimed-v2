// prisma/data/profileFields.data.ts
// The core GLOBAL profile fields every user answers up front (name, contact info,
// address, etc.) — consumed by prisma/seeders/profileFieldSeeder.ts.
//
// notConditional=true fields still load/answer like any other GLOBAL field, they're just
// excluded from the pickers used to build a benefit's eligibility rule tree (free-text
// name/address fields aren't meaningful eligibility comparators).
//
// "nationality"/"country" are seeded as SINGLE_SELECT with no options yet (countries.json
// load still pending), and "location" as HIERARCHY_SELECT with no hierarchy attached yet —
// both deferred to a follow-up pass (options editable from the admin Fields UI already).

export interface ProfileFieldOptionDef {
  englishName: string;
  tagalogName: string;
  // Overrides the default toSnakeCaseKey(englishName) value. Needed for options sourced
  // from an eGov additional_information enum we don't have the real backing code for (e.g.
  // Religion) — the raw string eGov sends back is all we have to match against, so the
  // option's value has to be that exact string, not a normalized guess at it.
  value?: string;
}

export interface ProfileFieldDef {
  englishName: string;
  tagalogName: string;
  englishDescription: string;
  tagalogDescription: string;
  inputType: string;
  required: boolean;
  sortOrder: number;
  configJson: Record<string, unknown> | null;
  notConditional: boolean;
  options?: ProfileFieldOptionDef[];
  // REPEATER_GROUP only — its row-level children (own DimField rows, parentFieldId set to
  // the parent). Must not themselves be REPEATER_GROUP (no nesting) — see field.service.ts's
  // assertNotNestedRepeaterGroup, mirrored by seedProfileFields.
  children?: ProfileFieldDef[];
}

